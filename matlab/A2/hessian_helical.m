function dx = hessian_helical(X)
  x1 = X(1);
  x2 = X(2);
  x3 = X(3);
  dx(1,1) = (200*x1*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) + (1000*x2*(x3 - (5*atan(x2/x1))/pi))/(x1^2*pi*(x2^2/x1^2 + 1));
  dx(1,2) = (200*x2*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) - (1000*(x3 - (5*atan(x2/x1))/pi))/(x1*pi*(x2^2/x1^2 + 1));
  dx(1,3) = 202*x3 - (1000*atan(x2/x1))/pi;

  dx(2,1) = (200*x1*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) + (1000*x2*(x3 - (5*atan(x2/x1))/pi))/(x1^2*pi*(x2^2/x1^2 + 1));
  dx(2,2) = (200*x2*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) - (1000*(x3 - (5*atan(x2/x1))/pi))/(x1*pi*(x2^2/x1^2 + 1));
  dx(2,3) = 202*x3 - (1000*atan(x2/x1))/pi;

  dx(3,1) = (200*x1*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) + (1000*x2*(x3 - (5*atan(x2/x1))/pi))/(x1^2*pi*(x2^2/x1^2 + 1));
  dx(3,2) = (200*x2*((x1^2 + x2^2)^(1/2) - 1))/(x1^2 + x2^2)^(1/2) - (1000*(x3 - (5*atan(x2/x1))/pi))/(x1*pi*(x2^2/x1^2 + 1));
  dx(3,3) = 202*x3 - (1000*atan(x2/x1))/pi;

  return
end
